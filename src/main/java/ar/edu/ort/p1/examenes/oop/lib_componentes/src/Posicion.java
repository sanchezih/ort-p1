package ar.edu.ort.p1.examenes.oop.lib_componentes.src;

public class Posicion {
	private int x;
	private int y;

	public Posicion(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
