package ar.edu.ort.p1.unidades.unidad04.ejemplos.ds.linear._01_array._03_posicionamiento_indirecto.src;

public class Vendedor {

	private String legajo;
	private String nombre;

	/*----------------------------------------------------------------------------*/

	public Vendedor(String legajo, String nombre) {
		this.legajo = legajo;
		this.nombre = nombre;
	}

	/*----------------------------------------------------------------------------*/

	public String getLegajo() {
		return legajo;
	}

	public String getNombre() {
		return nombre;
	}

}
